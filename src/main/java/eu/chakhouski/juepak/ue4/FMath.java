package eu.chakhouski.juepak.ue4;

public class FMath
{
    public static long Min(long a, long b)
    {
        return Math.min(a, b);
    }

    public static int Min(int a, int b)
    {
        return Math.min(a, b);
    }


    public static long Max(long a, long b)
    {
        return Math.max(a, b);
    }

    public static int Max(int a, int b)
    {
        return Math.max(a, b);
    }
}
